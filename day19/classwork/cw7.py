#მომხმარებელს შეეკითხეთ მისი გვარი, შემდეგ კითხეთ სურს თუ არა გვარის შემობრუნება და თუ დაგიბრუნათ "yes" მაშინ დაუბეჭდეთ მისი გვარი შემობრუნებული, თუ დაგიბრუნათ "no", მაშინ დაუბეჭდეთ მისი გვარი ჩვეულებრივად.
text = input("შეიყვანე ტექსტი: ")

choice = input("გინდა შემობრუნება? (yes/no): ")

if choice == "yes":
    print(text[::-1])
else:
    print(text)
