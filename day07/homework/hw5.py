operatiors = input("შეიყვანეთ ოპერატიული (+,-,*,/)")
Num1 = float(input("შეიყვანეთ ახლანდელი წელი:" ))
Num2 = float(input("შეიყვანეთ დაბადების წელი:" ))


if operatiors == "+":
    print(Num1 + Num2)

elif operatiors == "-":
    print(Num1 - Num2)

elif operatiors =="/":
    print(Num1 / Num2)

elif operatiors =="*":
    print(Num1 * Num2)